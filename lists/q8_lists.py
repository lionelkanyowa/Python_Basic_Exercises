# Q.8: How can you check if a variable holds a value that is a list? Given two variables, verify whether the values
# they hold are lists.

some_value1 = [0, 1, 0, 0, 1]
some_value2 = 'I leave my Kingdom, take care of it.'

print(type(some_value1) is list)
print(type(some_value2) is list)

